// FIFO, LIFO

// Queue - first in, first out
// push/shift
const queue = []
queue.push(4)
queue.push(7)
queue.push(9)

console.log(queue)

queue.shift()
console.log(queue)

// Stack - first in, last out
// push/pop
const stack = []
stack.push(5)
stack.push(3)
stack.push(2)
stack.push(1)

console.log(stack)

stack.pop()
stack.pop()

console.log(stack)